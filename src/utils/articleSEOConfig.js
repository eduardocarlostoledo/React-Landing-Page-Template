/**
 * Función para generar Schema Article
 */
export const generateArticleSchema = (articleData) => {
  return {
    "@context": "https://schema.org",
    "@type": "Article",
    "headline": articleData.title,
    "description": articleData.description,
    "image": articleData.imageUrl,
    "author": {
      "@type": "Organization",
      "name": "Toledo Consultora IT",
      "url": "https://toledoconsultora.com",
      "logo": "https://toledoconsultora.com/logo.png"
    },
    "publisher": {
      "@type": "Organization",
      "name": "Toledo Consultora IT",
      "logo": {
        "@type": "ImageObject",
        "url": "https://toledoconsultora.com/logo.png"
      }
    },
    "datePublished": new Date().toISOString(),
    "dateModified": new Date().toISOString(),
    "mainEntity": {
      "@type": "Article",
      "name": articleData.title,
      "description": articleData.description
    }
  };
};

/**
 * Función para generar FAQ Schema
 */
export const generateFAQSchema = (faqs) => {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    "mainEntity": faqs.map(faq => ({
      "@type": "Question",
      "name": faq.question,
      "acceptedAnswer": {
        "@type": "Answer",
        "text": faq.answer
      }
    }))
  };
};
